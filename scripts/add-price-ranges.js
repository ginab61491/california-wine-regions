// Default price ranges by tier + region
function defaultRange(producer) {
  const { price, tier, country, region } = producer;

  // Icon/luxury tiers
  if (price === 'luxury') {
    if (tier === 'legendary') return '$150-$500+';
    if (tier === 'iconic') return '$80-$300';
    return '$60-$200';
  }

  // Premium
  if (price === 'premium') {
    if (tier === 'iconic') return '$50-$150';
    if (tier === 'legendary') return '$80-$250';
    if (tier === 'established') return '$30-$80';
    return '$25-$75';
  }

  // Mid-range
  if (price === 'mid-range') {
    if (tier === 'established') return '$20-$50';
    if (tier === 'iconic') return '$25-$60';
    if (tier === 'rising') return '$15-$40';
    return '$15-$35';
  }

  // Moderate
  if (price === 'moderate') {
    return '$12-$25';
  }

  // Budget
  if (price === 'budget') {
    return '$8-$18';
  }

  return '$15-$50';
}

